const oldIcons = Object.keys(icons);

const Service = require('../../models/Service.js');
const Component = require('../../models/Component.js');

module.exports = {
	changeAllTheIcons: (req, res, next) => {
		Service.findAll().then(async services => {
			for (const service_ of services) {
				if (oldIcons.includes(service_.icon.trim())) {
					service_.icon = `fa-${icons[service_.icon.trim()]}`;
					await service_.save();
				}
			}
			Component.findAll().then(async components => {
				for (const component_ of components) {
					if (oldIcons.includes(component_.icon.trim())) {
						component_.icon = `fa-${icons[component_.icon.trim()]}`;
						await component_.save();
					}
				}
				return res.json({success: true});
			}).catch(err => {
				next(err);
				return null;
			});
		}).catch(err => {
			next(err);
			return null;
		});
	}
};

